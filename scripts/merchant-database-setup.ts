async function getAuth(): Promise<JWT> {
  // Try environment variable first
  const keyEnv = process.env.GOOGLE_SERVICE_ACCOUNT_KEY;
  if (keyEnv) {
    const keyData = JSON.parse(keyEnv);
    return new JWT({
      email: keyData.client_email,
      key: keyData.private_key,
      scopes: ['https://www.googleapis.com/auth/spreadsheets'],
    });
  }

  // Try file-based credentials
  const credPaths = [
    path.join(__dirname, '..', 'credentials', 'service-account.json'),
    path.join(__dirname, '..', 'service-account.json'),
    process.env.GOOGLE_APPLICATION_CREDENTIALS || '',
  ].filter(Boolean);

  for (const p of credPaths) {
    if (fs.existsSync(p)) {
      const keyData = JSON.parse(fs.readFileSync(p, 'utf8'));
      return new JWT({
        email: keyData.client_email,
        key: keyData.private_key,
        scopes: ['https://www.googleapis.com/auth/spreadsheets'],
      });
    }
  }

  throw new Error(
    'No Google credentials found. Set GOOGLE_SERVICE_ACCOUNT_KEY env var ' +
      'or place service-account.json in ./credentials/ directory.\n' +
      'The service account must have the Google Sheet shared with its email address.'
  );
}

async function setupSheets(sheets: sheets_v4.Sheets): Promise<void> {
  console.log('📋 Getting existing sheets...');

  const spreadsheet = await sheets.spreadsheets.get({
    spreadsheetId: SPREADSHEET_ID,
    fields: 'sheets.properties',
  });

  const existingSheets = spreadsheet.data.sheets?.map((s) => s.properties?.title) || [];
  console.log(`   Found ${existingSheets.length} existing sheets: ${existingSheets.join(', ')}`);

  const allSheetNames = [...CATEGORY_SHEETS.map((s) => s.title), ...MANAGEMENT_SHEETS];

  const sheetsToCreate = allSheetNames.filter((name) => !existingSheets.includes(name));

  if (sheetsToCreate.length > 0) {
    console.log(`   Creating ${sheetsToCreate.length} new sheets...`);
    const requests: sheets_v4.Schema$Request[] = sheetsToCreate.map((title) => ({
      addSheet: {
        properties: { title },
      },
    }));

    await sheets.spreadsheets.batchUpdate({
      spreadsheetId: SPREADSHEET_ID,
      requestBody: { requests },
    });
    console.log('   ✅ Sheets created successfully');
  } else {
    console.log('   ✅ All sheets already exist');
  }
}

