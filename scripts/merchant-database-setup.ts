function classifyPriority(rating: number, reviewCount: number): { priority: string; wave: string } {
  if (rating >= 4.0 && reviewCount >= 20) {
    return { priority: 'أولوية عالية', wave: 'موجة 1' };
  }
  if (rating >= 3.5 || reviewCount >= 10) {
    return { priority: 'أولوية متوسطة', wave: 'موجة 2' };
  }
  return { priority: 'أولوية منخفضة', wave: 'موجة 3' };
}

