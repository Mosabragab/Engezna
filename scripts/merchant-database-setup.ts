async function getAuth(): Promise<JWT> {
  // Try environment variable first
  const keyEnv = process.env.GOOGLE_SERVICE_ACCOUNT_KEY;
  if (keyEnv) {
    const keyData = JSON.parse(keyEnv);
    return new JWT({
      email: keyData.client_email,
      key: keyData.private_key,
      scopes: ['https://www.googleapis.com/auth/spreadsheets'],
    });
  }

  // Try file-based credentials
  const credPaths = [
    path.join(__dirname, '..', 'credentials', 'service-account.json'),
    path.join(__dirname, '..', 'service-account.json'),
    process.env.GOOGLE_APPLICATION_CREDENTIALS || '',
  ].filter(Boolean);

  for (const p of credPaths) {
    if (fs.existsSync(p)) {
      const keyData = JSON.parse(fs.readFileSync(p, 'utf8'));
      return new JWT({
        email: keyData.client_email,
        key: keyData.private_key,
        scopes: ['https://www.googleapis.com/auth/spreadsheets'],
      });
    }
  }

  throw new Error(
    'No Google credentials found. Set GOOGLE_SERVICE_ACCOUNT_KEY env var ' +
    'or place service-account.json in ./credentials/ directory.\n' +
    'The service account must have the Google Sheet shared with its email address.'
  );
}

